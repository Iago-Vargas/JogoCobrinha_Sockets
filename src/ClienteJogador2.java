    private void jButtonFrutaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jButtonFrutaKeyPressed
        switch (evt.getKeyCode()) {
            case 37: Movimenta.moveLeft(jButtonJogador2); break;
            case 38: Movimenta.moveUp(jButtonJogador2); break;
            case 39: Movimenta.moveRight(jButtonJogador2, jPanelJogador2.getBounds().width); break;
            case 40: Movimenta.moveDown(jButtonJogador2, jPanelJogador2.getBounds().height); break;
        }

        if (Movimenta.intersects(jButtonJogador2, jButtonFruta)) {
            Movimenta.addPoint(2, txtPlacar);
            Movimenta.randomPlace(
                jButtonFruta,
                jPanelJogador2.getBounds().width,
                jPanelJogador2.getBounds().height
            );
            Movimenta.randomizeObstacles(
                barreiras,
                jPanelJogador2.getBounds().width,
                jPanelJogador2.getBounds().height
            );

            if (!Movimenta.isMatchOver()) {
                Movimenta.randomPlace(
                    jButtonFruta,
                    jPanelJogador2.getBounds().width,
                    jPanelJogador2.getBounds().height
                );
                try {
                    c = new Componente(
                        jButtonFruta.getBounds().x,
                        jButtonFruta.getBounds().y,
                        jButtonFruta.getBounds().width,
                        jButtonFruta.getBounds().height
                    );
                    c.kind = Componente.FRUIT;
                    comunicador.sendPacket(c);
                } catch (Exception e) {
                    System.out.println("Erro ao enviar a fruta");
                }
            }
        }

        // Envia posição do jogador2 ao servidor
        try {
            c = new Componente(
                jButtonJogador2.getBounds().x,
                jButtonJogador2.getBounds().y,
                jButtonJogador2.getBounds().width,
                jButtonJogador2.getBounds().height
            );
            c.kind = Componente.PLAYER;
            comunicador.sendPacket(c);
        } catch (Exception e) {
            System.out.println("Erro ao enviar o jogador 2");
        }
    }//GEN-LAST:event_jButtonFrutaKeyPressed

