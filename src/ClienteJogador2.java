    public ClienteJogador2() {
         initComponents();

        barreiras = java.util.Arrays.asList(jButton1, jButton2, jButton3, jButton4);
        Movimenta.setObstacles(barreiras);

        new Thread() {
            @Override
            public void run() {
                try {
                    String host = "**"; 
                    int porta = 12345;
                    socket_jogador1 = new Socket(host, porta);

                    comunicador = new Comunicador(socket_jogador1);

                    while (true) {
                        c = comunicador.readPacket();
                        if (c.kind == Componente.PLAYER) {
                            jButtonJogador1.setBounds(c.posX, c.posY, c.width, c.height);
                        } else if (c.kind == Componente.FRUIT) {
                            jButtonFruta.setBounds(c.posX, c.posY, c.width, c.height);
                            Movimenta.addPoint(1, txtPlacar);
                            Movimenta.randomizeObstacles(
                                barreiras,
                                jPanelJogador2.getBounds().width,
                                jPanelJogador2.getBounds().height
                            );
                        } else if (c.kind == Componente.RESET) {
                            Movimenta.resetMatch(
                                txtPlacar,
                                jButtonFruta,
                                jPanelJogador2.getBounds().width,
                                jPanelJogador2.getBounds().height
                            );
                            Movimenta.randomizeObstacles(
                                barreiras,
                                jPanelJogador2.getBounds().width,
                                jPanelJogador2.getBounds().height 
                            );
                        }
                    }
                } catch (Exception e) {
                    System.out.println("Erro: " + e.getMessage());
                }
            }
        }.start();
    }

